def soma(n1:float, n2:float):
    soma = n1 + n2
    return soma

def subtracao(n1:float, n2:float):
    subtracao = n1 - n2
    return subtracao

def divisao(n1:float, n2:float):
    divisao = n1/n2
    return divisao

def multiplicacao(n1:float, n2:float):
    multiplicacao = n1 * n2
    return multiplicacao

def potencia(n1:float, n2:float):
    potencia = n1 ** n2
    return potencia
